/**
 * this class represents a Taw in game
 *
 * @author Amir Naziri
 */
public class Taw
{
    private String color; // color of Taw


    /**
     * creates a new Taw by default color : white
     */
    public Taw ()
    {
        this.color = "white";
    }


    /**
     * @return color
     */
    public String getColor () {
        return color;
    }

    /**
     * sets new color
     * @param color color
     */
    public void setColor (String color)
    {
        this.color = color;
    }





}
